package atenea.fiuba.algoIII.ageoOfEmpires;

public class Posicion {
    private int ejeX;
    private int ejeY;

    public Posicion(int x, int y){
        this.ejeX = x;
        this.ejeY = y;
    }

    public int getEjeX(){
        return this.ejeX;
    }

    public int getEjeY(){
        return this.ejeY;
    }
}
